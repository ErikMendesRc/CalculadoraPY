class Calculator:

    def __init__(self, a, b):
        self.a = a
        self.b = b

    def sum(self):
        return self.a + self.b

    def multiplication(self):
        return self.a * self.b

    def subtract(self):
        return self.a * self.b

    def divider(self):
        return self.a / self.b
